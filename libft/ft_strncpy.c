#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	char *s2;

	s2 = dest;
	while (n > 0 && *src != '\0')
	{
		*dest++ = *src++;
		n--;
	}
	while (n > 0)
	{
		*dest++ = '\0';
		n--;
	}
	return (s2);
}
