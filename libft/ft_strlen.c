#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;
	size_t	cnt;

	cnt = 0;
	i = 0;
	while (s[i] != '\0')
	{
		cnt++;
		i++;
	}
	return (cnt);
}
